  <h2>Qualidade do Ar Indoor</h2>
  <p><img src="<?=base_url()?>/images/content/servicos/ar/ar-analise.jpg" alt="Imagem - Análise do Ar" title="Imagem - Análise do Ar" width="245" height="185" class="imgfloatright"/>A Biológica realiza o monitoramento da qualidade do ar em ambientes fechados, de uso público e coletivo, salas limpas e demais ambientes com sistemas de climatização. </p>
  <p>A avaliação da qualidade do ar destes ambientes é realizada através de coleta de amostras com utilização de equipamentos apropriados e execução das análises microbiológicas e físico químicas estabelecidas na Resolução RE 09 de 20/01/2003 da ANVISA.</p>
  <h2>Monitoramento de Emissões Atmosféricas</h2>
