  <p> Para o monitoramento hídrico, a Biológica disponibiliza os seguintes serviços:  <br />
 Análises microbiológicas, análises físico-quimicas, análises ecotoxicológicas com microcrustáceos , análises limnológicas, consultorias e elaboração de relatórios técnicos para órgãos ambientais e de saneamento.
