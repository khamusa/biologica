  <p><img src="<?=base_url()?>/images/content/servicos/agua/analise-agua-processo.jpg" alt="Imagem - Análise Fisicoquímica Água e Efluentes" title="Imagem - Análise Fisicoquímica Água e Efluentes" class="imgfloatright" width="194" height="256"/>A Biológica realiza o monitoramento hídrico em amostras de águas superficiais, subterrâneas, efluentes industriais, entre outros. Através de uma logística eficiente, atendemos clientes em todo o país.</p>
