  <h1><a name="ecotoxicidade" id="agua2"></a> Ecotoxicidade</h1>
  <p>Os testes de toxicidade são empregados para avaliar um efluente como um todo. Eles consistem da exposição de organismos aquáticos (representativos do ambiente) a várias concentrações de uma amostra ambiental ou substâncias ou a fatores ambientais durante um determinado período de tempo.</p>
  <p>Os efeitos observados traduzem o resultado final das ações aditivas, antagônicas e sinérgicas das substâncias disponíveis na amostra. Os critérios de avaliação são: a mobilidade, a letalidade e a reprodução.</p>
